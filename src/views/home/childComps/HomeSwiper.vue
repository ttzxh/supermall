<template>
	<swiper>
		<swiper-item v-for="item in banners">
		  <a :href="item.link">
				<img :src="item.image" alt="" @load="imageLoad">
			</a>
			
		</swiper-item>
	</swiper>
	
</template>

<script>
  import {Swiper,SwiperItem} from 'components/common/swiper/index.js'
	
	export default{
		name:"HomeSwiper",
		props:{
			banners:{
				type:Array,
				default(){
					return []
				}
			}
		},
		data(){
			return{
				isLoad:false
			}
		},
		components:{
			Swiper,
			SwiperItem
		},	
		methods:{
			// 让轮播图的图片加载玩之后再进行计算tabControl的offsetTop
			imageLoad(){
				// console.log('333')
				if(!this.isLoad){
					this.$emit('swiperImageLoad')
					this.isLoad=!this.isLoad
				}
				
			}
		}
		
		
	}
</script>

